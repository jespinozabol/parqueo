    private void jbtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnRegistrarActionPerformed
        if (jtxtplaca.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Faltan datos");
        } else {
            try {
                Calendar cal = Calendar.getInstance();
                int hora = cal.get(cal.HOUR_OF_DAY);
                int minu = cal.get(cal.MINUTE);
                int xy = (9 * fila) + columna;
                usr = new Usuario(jtxtplaca.getText(), jtxtcedula.getText(), jtxtnombre.getText(), hora, minu);
                Mng.OcuparCampo(xy, usr);
                jtEstacionamiento.setValueAt("Ocupado", fila, columna);
                estadosEstacionamiento[fila][columna] = estadosEstacionamiento[fila][columna] == "Ocupado" ? "Disponible" : "Ocupado";
                jDialog1.dispose();
                sendMessage();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "la hora va de 0 a 23 y los minutos de 0 a 59");
            }
        }
    }//GEN-LAST:event_jbtnRegistrarActionPerformed

    public int SaveFile(int precio) throws IOException {
        Calendar cal = Calendar.getInstance();
        int year = cal.get(Calendar.YEAR);
        int month = cal.get(Calendar.MONTH);
        int day = cal.get(Calendar.DAY_OF_MONTH);
        int hour = cal.get(Calendar.HOUR_OF_DAY);
        
        String fileName = hour + "-" + day + "-" + year + ".txt";
        String mensaje = precio + "";

        Path path = Paths.get(fileName);

        File f = new File(fileName);

        PrintWriter out = null;
        if (f.exists() && !f.isDirectory()) {
            out = new PrintWriter(new FileOutputStream(new File(fileName), true));
        } else {
            out = new PrintWriter(fileName);
        }
        out.append(mensaje);
        out.append(Constants.NEWLINE);
        out.close();

        return 0;
    }
