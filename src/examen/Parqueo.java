    private void Reloj() {
        int totalEspacios = fila * columna;
        new Thread() {
            @Override
            public void run() {
                while (true) {
                    try {
                        int vacios = totalEspacios - Mng.Libres();
                        jlbocupado.setText("" + vacios);
                        jlbLibre.setText("" + Mng.Libres());
                        jlbcosto.setText("" + Mng.getPrecio());
                        jlbhora.setText(formatohora.format(new Date()));
                        sleep(500);                        
                    } catch (InterruptedException ex) {
                    }
                }
            }
        }.start();
    }

