    private void SetJtEspacios() throws NumberFormatException {
        int value = Integer.parseInt(val);
        Manager m = new Manager(value);
        columna = value;//prop.getProperty("pisos")!=val?value:Integer.parseInt(prop.getProperty("pisos"));
        fila = value;//prop.getProperty("espacios")!=val?value:Integer.parseInt(prop.getProperty("espacios"));
        
        Object[][] rows;
        rows = new Object[columna][fila];
        String[] headers;
        headers = new String[columna];
        estadosEstacionamiento = new String[columna][fila];
        for (int i = 0; i < columna; i++) {
            headers[i] = "Piso" + (i + 1);
            for (int j = 0; j < fila; j++) {
                rows[i][j] = "Disponible";
            }
        }
        jtEstacionamiento.setModel(new DefaultTableModel(rows, headers));
        jtEstacionamiento.setLayout(new GridLayout());
        jtEstacionamiento.setCellSelectionEnabled(true);

        for (int i = 0; i < columna; i++) {
            jtEstacionamiento.getColumnModel().getColumn(i).setCellRenderer(new CustomRenderer());
            for (int j = 0; j < fila; j++) {
                estadosEstacionamiento[i][j] = "Ocupado";
            }
        }
    }

