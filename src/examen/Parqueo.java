    private void jtEstacionamientoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtEstacionamientoMousePressed
        fila = jtEstacionamiento.rowAtPoint(evt.getPoint());
        columna = jtEstacionamiento.columnAtPoint(evt.getPoint());
        jtEstacionamiento.changeSelection(fila, columna, true, true);
        if (estadosEstacionamiento[fila][columna] == "Ocupado") {
            jDialog1.setVisible(true);
        } else {
            jDialog1.dispose();
            int i = JOptionPane.showConfirmDialog(null, "Desea Cancelar su Factura", "Cancelar", JOptionPane.YES_NO_OPTION);
            if (i == JOptionPane.YES_OPTION) {
                jButton1ActionPerformed();
            }
        }
    }//GEN-LAST:event_jtEstacionamientoMousePressed

